   $person = array("name" => "John", "age" => 30);
   

3. Multidimensional arrays: These are arrays that contain other arrays as their elements. They can be used to represent complex data structures, such as tables or matrices. Example:
   $matrix = array(
       array(1, 2, 3),
       array(4, 5, 6),
       array(7, 8, 9)
   );
   
